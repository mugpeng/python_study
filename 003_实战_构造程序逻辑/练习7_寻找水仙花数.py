'''
找出所有的水仙花数
说明：水仙花数也被称为超完全数字不变数、自恋数、自幂数、阿姆斯特朗数，
它是一个3位数，该数字每个位上数字的立方之和正好等于它本身，例如：$1^3 + 5^3+ 3^3=153$。
'''

for n in range(100, 1000):
    low = n % 10
    mid = n % 100 // 10
    high = n // 100 # 整除
    if n == low ** 3 + mid ** 3 + high ** 3:
        print('%d 是一个水仙花数' % n)

'''
方法二
用字符串的切片性质
'''

for n in range(100, 1000):
    b = str(n)
    low = int(b[2])
    mid = int(b[1])
    high = int(b[0])
    if n == low ** 3 + mid ** 3 + high ** 3:
        print('%d 是一个水仙花数' % n)